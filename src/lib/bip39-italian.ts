export function generateSeedPhrase(): string {
  const array = new Uint32Array(12);
  const cryptoObj = window.crypto || (window as any).msCrypto;
  cryptoObj.getRandomValues(array);
  const words: string[] = [];
  for (let i = 0; i < 12; i++) {
    const index = array[i] % BIP39_ITALIAN.length;
    words.push(BIP39_ITALIAN[index]);
  }
  return words.join(" ");
}

export function validateSeedPhrase(phrase: string): boolean {
  const parts = phrase.trim().toLowerCase().split(/\s+/);
  if (parts.length !== 12) return false;
  return parts.every(word => BIP39_ITALIAN.includes(word));
}
